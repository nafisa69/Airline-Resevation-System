     public void  autoId(){
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con =DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","admin12!@");
                pre=con.prepareStatement("select MAX(adminID) as amaxId from admin");
            ResultSet rs=pre.executeQuery();
            rs.next();
            if(rs.getString("amaxId")==null){
                aId.setText("AD101");
            }
            else{
                long id=Long.parseLong(rs.getString("amaxId").substring(2,rs.getString("amaxId").length()));
                id++;
                aId.setText("AD"+String.format("%03d", id));
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
